    [SerializeField] private MonsterSpawner spawner;
    [SerializeField] private GameObject effectSystem;
    [SerializeField] private SceneManagement SceneManager;
    [SerializeField] private Text waveText;
    [SerializeField] private Text LeftMonster;
    private int stage;
    private int order;

    public int monsterCount;
    [SerializeField] private CostControl Cost;

    void Start()
    {
        order = 0;
        stage = PlayerPrefs.GetInt("StageLevel");

        effectSystem.GetComponent<EffectSystem>().Guide(waveText, "Wave " + (stage + 1), 4);
        for (int i = 0; i < 60; i++)
        {
            if (wave[stage, 0, i] == 1) monsterCount++;
            if (wave[stage, 1, i] == 1) monsterCount++;
        }
        LeftMonster.text = "남은 적 : " + monsterCount.ToString();
        StartCoroutine("WaveStart");
    }

    IEnumerator WaveStart()
    {
        yield return new WaitForSeconds(5);
        while (true)
        {
            if (wave[stage, 0, order] == 1) { spawner.SpawnLeft(); }
            if (wave[stage, 1, order] == 1) { spawner.SpawnRight(); }
            order++;
            if (order == 60)
            {
                StopCoroutine("WaveStart");
            }
            yield return new WaitForSeconds(1);
        }
    }

    public void Death()
    {
        monsterCount--;
        Cost.GSCost(0.5f);
        LeftMonster.text = "남은 적 : " + monsterCount.ToString();
        if (monsterCount <= 0)
        {
            SceneManager.LoadEndGame();
        }
    }
